     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $request->validate([
            'email' => 'required',
            'name' => 'required'
        ]);
        
        Application::create($data);
  
        return redirect()->route('index');
    }

